class CarModel {
  final String id;
  final String name;
  final String specs;
  final int pricePerDay;
  final bool available;
  final String image;

  CarModel({
    required this.id,
    required this.name,
    required this.specs,
    required this.pricePerDay,
    required this.available,
    required this.image,
  });
}
